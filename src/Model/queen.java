package Model;

public class queen {
	

	

	/* constructor && getters && setters method */
	private int xindex;
	private int yindex;
	public queen(int xindex, int yindex) {

		super();
		this.xindex = xindex;
		this.yindex = yindex;
	}
	public int getXindex() {
		return xindex;
	}
	public void setXindex(int xindex) {
		this.xindex = xindex;
	}
	public int getYindex() {
		return yindex;
	}
	public void setYindex(int yindex) {
		this.yindex = yindex;
	}

	
}
